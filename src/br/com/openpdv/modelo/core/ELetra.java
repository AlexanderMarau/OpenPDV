package br.com.openpdv.modelo.core;

/**
 * Enumerador que define os meios de padronização das letras.
 *
 * @author Pedro H. Lira
 */
public enum ELetra {

    /**
     * Campo que define como GRANDE o padrao de letras.
     */
    GRANDE,
    /**
     * Campo que define como NORMAL o padrao de letras
     */
    NORMAL,
    /**
     * Campo que define como PEQUENA o padrao de letras
     */
    PEQUENA;
}
